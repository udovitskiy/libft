#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list *lst_elem;

	if (!(lst_elem = malloc(sizeof(t_list))))
		return (NULL);
	lst_elem->content = content;
	lst_elem->next = NULL;
	return (lst_elem);
}
